type TextKey = keyof typeof UI_TEXT.en;

export function text(language: AppLanguage, key: TextKey, values: Record<string, string | number> = {}): string {
  let template: string = UI_TEXT[language][key] ?? UI_TEXT.en[key];
  for (const [name, value] of Object.entries(values)) {
    template = template.split(`{${name}}`).join(String(value));
  }
  return template;
}
